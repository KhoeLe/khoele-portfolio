export async function getBagData() {
  /// get data from BagData and group to categories
  const categories: { [key: string]: any[] } = BagData.reduce((acc, item) => {
    if (!acc[item.category]) {
      acc[item.category] = []
    }
    acc[item.category].push(item)
    return acc
  }, {} as { [key: string]: any[] }) // Add index signature to allow indexing with a string


  return categories
}